    private void txtAsdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAsdKeyReleased
        if (contAux == 0) {
            contAux = 1;
            new Thread(new Runnable() {

                @Override
                public void run() {
                    for (int i = 5; i > 0; i--) {
                        try {
                            lblReloj.setText(String.valueOf(i));
                            Thread.sleep(1000);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }

                    lblReloj.setText("Se termin√≥ el tiempo!!");
                    txtAsd.setEditable(false);

                }
            }).start();
        }

        if (txtAsd.isEditable()) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                if (txtAsd.getText().equals(palabra)) {
                    contAsd++;

                    lblCont.setText(String.valueOf(contAsd));
                   
                    String texto = "";
                    for(int i=1; i<=contAsd; i++){
                        texto += i+") "+palabra+"<br>";
                    }
                    
                    txtLog.setText(texto);
                }
                txtAsd.setText(null);
                txtAsd.requestFocus();
            }
        }

    }//GEN-LAST:event_txtAsdKeyReleased

